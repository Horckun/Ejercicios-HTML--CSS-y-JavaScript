// AVERIGUAR NOMBRE DEL CÓDIGO DEL NAVEGADOR
var app = navigator.appCodeName;
// mostrar en pantalla
document.write("Código de navegador: "+app+".");
// SEPARACIÓN
document.write("<hr>");
// AVERIGUAR MARCA COMERCIAL DEL NAVEGADOR
var marca = navigator.appName;
// mostrar en pantalla
document.write("Marca: "+marca+".");
// SEPARACIÓN
document.write("<hr>");
// AVERIGUAR IDIOMA DEL NAVEGADOR
var idioma = navigator.language;
// mostrar en pantalla
document.write("Idioma: "+idioma+".");
// SEPARACIÓN
document.write("<hr>");
// AVERIGUAR PLATAFORMA EN LA QUE TRABAJA EL NAVEGADOR
var plataforma = navigator.platform;
// mostrar en pantalla
document.write("Plataforma: "+plataforma+".");
// SEPARACIÓN
document.write("<hr>");
// AVERIGUAR CANTIDAD DE PLUGINS INSTALADOS EN EL NAVEGADOR
var plugins = navigator.plugins.length;
// mostrar en pantalla
document.write("Plugins: "+plugins+".");
// SEPARACIÓN
document.write("<hr>");
// AVERIGUAR CANTIDAD DE MIMES INSTALADOS EN EL NAVEGADOR
var mime = navigator.mimeTypes.length;
// mostrar en pantalla
document.write("Mimes: "+mime+".");
// SEPARACIÓN
document.write("<hr>");
