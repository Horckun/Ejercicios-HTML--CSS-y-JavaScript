// Declarar variable
var num = 8;
// Menú de selección
switch (num) {
  case 2:
    alert("El valor es menor de 5.");
    break;
  case 5:
    alert("El valor es 5.");
    break;
  case 8:
    alert("El valor es mayor de 5.");
    break;
  default:
    alert("No se ha detectado ningún número.");
    break;
}
